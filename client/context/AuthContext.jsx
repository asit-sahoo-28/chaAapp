import {  createContext, useEffect, useState } from "react";
import toast from "react-hot-toast";
import axios from "axios";
import {io} from "socket.io-client";


const backendUrl=import.meta.env.VITE_BACKEND_URL;
axios.defaults.baseURL=backendUrl;
export const AuthContext=createContext();
export const AuthProvider=({children})=>{

    const [token,setToken]=useState(localStorage.getItem("token"));
    const  [authUser,setAuthUser]=useState(null);
    const [onlineUsers,setOnlineUsers]=useState([]);
    const [socket,setSocket]=useState(null);

    const checkAuth=async()=>{
        try{
              const {data}=await axios.get("/api/auth/check");
              if(data.success){
                setAuthUser(data.user);
                connectSocket(data.user);
              }
        }catch(error){
        toast.error(error.message);
        }
    }

  



 const login = async (state, credentials) => {
  try {
    // Send POST request to backend
    const { data } = await axios.post(`/api/auth/${state}`, credentials);

    // Check if backend responded with success
    if (data.success) {
      // Save authenticated user
      setAuthUser(data.userData);

      // Connect to socket
      connectSocket(data.userData);

      // Set Authorization header for future requests
      axios.defaults.headers.common["Authorization"] = `Bearer ${data.token}`;

      // Save token in state and localStorage
      setToken(data.token);
      localStorage.setItem("token", data.token);

      // Show success message
      toast.success(data.message);
    } else {
      toast.error(data.message || "Authentication failed");
    }
  } catch (error) {
    // Show detailed error from backend if available
    if (error.response && error.response.data && error.response.data.message) {
      toast.error(error.response.data.message);
    } else {
      toast.error(error.message || "Something went wrong");
    }
  }
};



    //Logout function to handle user logout and socket disconnection

    const logout=async()=>{
        localStorage.removeItem("token");
        setToken(null);
        setAuthUser(null);
        setOnlineUsers([]);
        axios.defaults.headers.common["token"]=null
        toast.success("Logout successfully")
        socket.disconnect();
    }




    const upadteProfile = async (body) => {
  try {
    const token = localStorage.getItem("token");
    const { data } = await axios.put(
      "/api/auth/update-profile",
      body,
      {
        headers: {
          token: token
        }
      }
    );

    if (data.success) {
      setAuthUser(data.user);
      toast.success("Profile updated successfully");
    }
  } catch (error) {
    console.log(error.response?.data);  // ðŸ‘ˆ very useful for debugging
    toast.error(error.response?.data?.message || error.message);
  }
};




    //connect socket function to handle scoket connectiom and online users upadte
    const connectSocket=(userData)=>{
      if(!userData || socket?.connected) return;
      const newSocket=io(backendUrl,{
        query:{
            userId:userData._id,
        }

      });
      newSocket.connect();
      setSocket(newSocket);

      newSocket.on("getOnlineUsers",(userIds)=>{
        setOnlineUsers(userIds);
      })
    }
    useEffect(()=>{
     if(token){
        axios.defaults.headers.common["token"]=token;
     }
     checkAuth();
    },[])
    const value={
    axios,authUser,onlineUsers,socket,login,logout,upadteProfile
    }
    return(
        <AuthContext.Provider value={value}>
            {children}
        </AuthContext.Provider>
    )
}










